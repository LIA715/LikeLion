public class Constants {
    public static void main(String[] args) {

        //상수 (constants, final variables)
        //한 번 값이 할당 => 재할당 불가능
        // 전체를 대문자, 단어 간 구분은 _

        final double PI_VALUE = 3.14; //final 자료형 ~
        System.out.println(PI_VALUE);

        //PI = 3.1415~ 재정의 불가능
    }
}
